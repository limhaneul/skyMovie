package Board;

public class FileBean {

	private int  num;
	private int type;
	private String realsave;	
	private String showsave;
	private String path;
	
	
	
	
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getRealsave() {
		return realsave;
	}
	public void setRealsave(String realsave) {
		this.realsave = realsave;
	}
	public String getShowsave() {
		return showsave;
	}
	public void setShowsave(String showsave) {
		this.showsave = showsave;
	}
	

	
	
}
